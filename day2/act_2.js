// Assignment operators

// Task 6: Use the `+=` operator to add a number to a variable and log the result

{
    let v = 10
    v += 10
    
    console.log(v)
    
    
    // Output: 20
}

// Task 7: Use the `-=` operator to subtract a number from a variable and log the result

{
    let v = 10
    v -= 5
    
    console.log(v)
    
    
    // Output: 5
}